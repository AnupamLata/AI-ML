# methods of lists

nums = [1,2,3,6,8,4]

# I.append(val) -> add one element at the end
nums.append(7)
print(nums)


# I.insert(idx,val) -> insert element at idx
nums.insert(2, 10)
print(nums)


# I.sort()  -> arranges in increasing order
nums.sort()
print(nums)
#  or
nums.sort(reverse = True)
print(nums)


# i.reverse() -> reverse order
nums.reverse()
print(nums)

